import Papa from 'papaparse';
import { Product } from '../types/product';

export const parseCSVData = (csvText: string): Omit<Product, 'id'>[] => {
  const results = Papa.parse(csvText, {
    header: true,
    delimiter: ';',
    skipEmptyLines: true,
    transformHeader: (header: string) => {
      // Map CSV headers to our product fields
      const headerMap: Record<string, keyof Product> = {
        'CODIGO': 'codigo',
        'PROFORMA': 'proforma', 
        'FACTURA': 'factura',
        'DISPONIBILIDAD': 'disponibilidad',
        'DESCRIPCIÓN': 'descripcion',
        'DESCRIPCI�N': 'descripcion', // Handle encoding issues
        'LLEGADA': 'llegada',
        'SUCURSAL': 'sucursal',
        'CLIENTE': 'cliente',
        'LUGAR': 'lugar', // Nueva columna
        'ENLACE': 'link',
        'LINK': 'link'
      };
      
      return headerMap[header.trim()] || header.toLowerCase();
    }
  });

  return results.data.map((row: any) => ({
    codigo: (row.codigo || '').toString().trim(),
    proforma: (row.proforma || '').toString().trim(),
    factura: (row.factura || '').toString().trim(),
    disponibilidad: (row.disponibilidad || '').toString().trim(),
    descripcion: (row.descripcion || '').toString().trim(),
    llegada: (row.llegada || '').toString().trim(),
    sucursal: (row.sucursal || '').toString().trim(),
    cliente: (row.cliente || '').toString().trim(),
    lugar: (row.lugar || '').toString().trim(),
    link: (row.link || '').toString().trim()
  })).filter(product => product.codigo); // Filter out empty products
};

